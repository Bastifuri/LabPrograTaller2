    private void ElimBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ElimBotonActionPerformed
        try {
            String CafeElimin = (String) cafeElim.getSelectedItem();
            
            File archivo = new File("cafe.txt");
            List<String> lineasArchivo = Files.readAllLines(archivo.toPath());
            
            // Eliminar el café seleccionado de la lista
            lineasArchivo.remove(CafeElimin);
            
            // Guardar la lista actualizada en el archivo cafe.txt
            BufferedWriter bw = new BufferedWriter(new FileWriter("cafe.txt"));
            for (String linea : lineasArchivo) {
                bw.write(linea + "\n");
            }
            bw.close();
            
            // Eliminar el café seleccionado del JComboBox
            cafeElim.removeItem(CafeElimin);
    } catch (IOException ex) {
            Logger.getLogger(EliminarCafe.class.getName()).log(Level.SEVERE, null, ex);
        }



    }//GEN-LAST:event_ElimBotonActionPerformed

