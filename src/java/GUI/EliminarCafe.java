    public EliminarCafe() {
        initComponents();
        this.setLocationRelativeTo(null);
        try {
            // Abre el archivo de carreras en modo lectura
            FileReader fr = new FileReader("cafe.txt");
            BufferedReader br = new BufferedReader(fr);

            // Crea una lista para almacenar las carreras
            List<String> cafes = new ArrayList<String>();

            // Lee el archivo línea por línea y agrega cada carrera a la lista
            String linea;
            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split(",");
                String nomcafes = datos[0];
                cafes.add(nomcafes);
            }

            // Agrega las carreras al JComboBox
            for (String cafe : cafes) {
                cafeElim.addItem(cafe); 
            }

            // Cierra los streams de lectura
            br.close();
            fr.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }

